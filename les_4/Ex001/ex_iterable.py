# В Python термин "iterable" (итерируемый объект) относится к любому объекту, который может быть использован в цикле for.
# Иными словами, это объект, который способен возвращать свои элементы по одному, что позволяет пройтись по ним,
# используя, например, цикл for.
#
# Итерируемые объекты включают в себя множество типов данных Python, таких как:
#
# Списки (list)
# Кортежи (tuple)
# Строки (str)
# Словари (dict)
# Множества (set)


my_list = [1, 2, 3, 4]
for item in my_list:
    print(item)
